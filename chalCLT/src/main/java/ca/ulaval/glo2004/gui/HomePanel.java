    /**
     * @param evt
     */
    public void onOuvrirProjetExistantClicked(ActionEvent evt) {
        JFileChooser fileChooser = new JFileChooser();
        int choix = fileChooser.showOpenDialog(null);

        if (choix == JFileChooser.APPROVE_OPTION) {
            File fichierChoisi = fileChooser.getSelectedFile();
            String cheminFichier = fichierChoisi.getAbsolutePath();

            this.controleur.charger(cheminFichier);

            // Stockez le chemin du fichier actuellement ouvert dans votre MainWindow
            mainWindow = new MainWindow(controleur);
            mainWindow.setCheminFichierActuel(cheminFichier);
            changerVue();
        }
    }

    /**
     * @param evt
     */
    public void onCreerNouveauProjetClicked(ActionEvent evt) {
        this.controleur.nouveauProjet();
        mainWindow = new MainWindow(controleur);
        changerVue();
    }


